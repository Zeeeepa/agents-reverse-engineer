#!/usr/bin/env node

/**
 * Generate 36 frames of a rotating hollow circle for the installer animation.
 *
 * Each frame is 6 lines tall x 16 chars wide. Only the ring outline is drawn;
 * the interior is empty. A bright highlight spot orbits around the ring.
 *
 * Terminal chars are ~2:1 height:width, so 16 wide x 6 tall ≈ visual circle.
 */

import { writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const FRAMES_DIR = join(__dirname, '..', 'src', 'installer', 'frames');

const WIDTH = 16;
const HEIGHT = 6;
const NUM_FRAMES = 36;

// Density characters from lightest to heaviest
const DENSITY = ' .·:;oO@';

/**
 * Render one frame of the hollow rotating circle.
 * @param {number} highlightAngle - Angle in radians where the bright spot is
 * @returns {string} Multi-line frame string
 */
function renderFrame(highlightAngle) {
  const lines = [];
  const cx = (WIDTH - 1) / 2;   // center x
  const cy = (HEIGHT - 1) / 2;  // center y
  // Radii in character units (account for char aspect ratio)
  const rx = (WIDTH - 2) / 2;   // horizontal radius
  const ry = HEIGHT / 2;        // vertical radius

  // Ring thickness in normalized [0,1] space
  const ringInner = 0.70;
  const ringOuter = 1.05;

  for (let row = 0; row < HEIGHT; row++) {
    let line = '';
    for (let col = 0; col < WIDTH; col++) {
      // Normalized coordinates
      const nx = (col - cx) / rx;
      const ny = (row - cy) / ry;
      const r = Math.sqrt(nx * nx + ny * ny);

      // Only draw on the ring (between inner and outer radius)
      if (r < ringInner || r > ringOuter) {
        line += ' ';
        continue;
      }

      // Angle of this point on the ring
      const pointAngle = Math.atan2(ny, nx);

      // Angular distance from the highlight
      let dAngle = pointAngle - highlightAngle;
      while (dAngle > Math.PI) dAngle -= 2 * Math.PI;
      while (dAngle < -Math.PI) dAngle += 2 * Math.PI;

      // Highlight falloff — bright spot with smooth trail
      const spread = 0.8;
      const highlight = Math.exp(-(dAngle * dAngle) / (2 * spread * spread));

      // Base ring brightness (dim outline)
      const base = 0.15;
      const brightness = Math.min(1.0, base + highlight * 0.85);

      // Map to density character
      const idx = Math.floor(brightness * (DENSITY.length - 1));
      line += DENSITY[Math.max(0, Math.min(idx, DENSITY.length - 1))];
    }
    lines.push(line);
  }

  return lines.join('\n');
}

// Generate frames
mkdirSync(FRAMES_DIR, { recursive: true });

const frames = [];
for (let i = 0; i < NUM_FRAMES; i++) {
  const angle = (i / NUM_FRAMES) * 2 * Math.PI;
  const frame = renderFrame(angle);
  frames.push(frame);

  const framePath = join(FRAMES_DIR, `frame_${i + 1}.txt`);
  writeFileSync(framePath, frame, 'utf-8');
}

// Generate index.ts that exports all frames as a string array
const indexContent = `/**
 * 36-frame golden circle animation frames
 *
 * Each frame is ${HEIGHT} lines x ${WIDTH} chars representing a hollow ring
 * with a rotating highlight spot.
 *
 * Generated by scripts/generate-circle-frames.js
 */

export const GOLDEN_CIRCLE_FRAMES: string[] = ${JSON.stringify(frames, null, 2)};

export const FRAME_WIDTH = ${WIDTH};
export const FRAME_HEIGHT = ${HEIGHT};
`;

writeFileSync(join(FRAMES_DIR, 'index.ts'), indexContent, 'utf-8');

console.log(`Generated ${NUM_FRAMES} frames (${WIDTH}x${HEIGHT}) in ${FRAMES_DIR}`);

// Preview a few frames
for (const idx of [0, 9, 18, 27]) {
  console.log(`\nFrame ${idx + 1}:`);
  console.log(frames[idx]);
}
